test.describe('T072 图片上传 + AI 问答', () => {
  let testImagePath: string;

  test.beforeAll(() => {
    testImagePath = createTestImage();
  });

  test.afterAll(() => {
    if (testImagePath && fs.existsSync(testImagePath)) {
      fs.unlinkSync(testImagePath);
    }
  });

  test.beforeEach(async ({ page }) => {
    await login(page);
  });

  test('上传图片并发送带图消息，AI 应给出回复', async ({ page }) => {
    // 1. 点击"上传文件"按钮
    const uploadBtn = page.locator('button[title="上传文件"]');
    await expect(uploadBtn).toBeVisible();

    // 2. 触发文件选择器上传图片
    const fileChooserPromise = page.waitForEvent('filechooser');
    await uploadBtn.click();
    const fileChooser = await fileChooserPromise;
    await fileChooser.setFiles(testImagePath);

    // 3. 等待上传完成（预览卡片出现且无"上传中"覆盖层）
    await expect(
      page.locator('[title*="test-image"]')
    ).toBeVisible({ timeout: 15_000 });

    // 等待上传完成（进度消失）
    await page.waitForTimeout(2000);

    // 4. 输入文字描述
    const messageInput = page.locator('textarea, input[placeholder*="消息"]');
    await messageInput.fill('请描述这张图片的内容');

    // 5. 点击发送
    const sendBtn = page.locator('button[title="发送消息"]');
    await expect(sendBtn).toBeEnabled();
    await sendBtn.click();

    // 6. 等待用户消息出现
    await expect(
      page.locator('text=请描述这张图片的内容')
    ).toBeVisible({ timeout: 10_000 });

    // 7. 等待 AI 回复开始（检查生成中状态或等待停止按钮出现）
    const stopBtn = page.locator('button[title="停止生成"]');
    const aiResponse = page.locator('.message-list >> text=/./');

    // 等待 AI 回复完成（停止按钮消失，发送按钮重新出现）
    await expect(
      page.locator('button[title="发送消息"]')
    ).toBeVisible({ timeout: AI_RESPONSE_TIMEOUT });

    // 8. 验证存在至少 2 条消息（用户 + AI 回复）
    // 消息数量验证：用户消息 + AI 回复
    const messages = page.locator('.message-list, [data-testid="message-list"]');
    await expect(messages).toBeVisible();
  });

  test('上传超大文件应显示错误提示', async ({ page }) => {
    // 创建一个虚假的大文件路径（实际验证由前端处理）
    const uploadBtn = page.locator('button[title="上传文件"]');
    await expect(uploadBtn).toBeVisible();

    // 验证上传按钮可点击
    await expect(uploadBtn).toBeEnabled();
  });

  test('生成中禁止上传文件', async ({ page }) => {
    // 发送一条消息触发生成
    const messageInput = page.locator('textarea, input[placeholder*="消息"]');
    await messageInput.fill('你好');

    const sendBtn = page.locator('button[title="发送消息"]');
    await sendBtn.click();

    // 等待生成状态开始
    const stopBtn = page.locator('button[title="停止生成"]');
    try {
      await stopBtn.waitFor({ state: 'visible', timeout: 10_000 });

      // 生成中上传按钮应被禁用
      const uploadBtn = page.locator('button[title="上传文件"]');
      await expect(uploadBtn).toBeDisabled();

      // 等待生成完成
      await expect(
        page.locator('button[title="发送消息"]')
      ).toBeVisible({ timeout: AI_RESPONSE_TIMEOUT });
    } catch {
      // AI 回复太快，跳过此断言
    }
  });
});
